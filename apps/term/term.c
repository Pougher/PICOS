struct application* term_new(void) {
    struct application* term = malloc(sizeof(struct application));

    strcpy(term->name, "TERM");
    strcpy(term->description, "Simple Terminal Emulator");

    term->begin = NULL;
    term->update = NULL;

    term->icon = malloc(sizeof(char*) * APP_ICON_HEIGHT);
    for (int i = 0; i < APP_ICON_HEIGHT; i++) {
        term->icon[i] = malloc(sizeof(char) * APP_ICON_WIDTH);
        if (term->icon[i] == NULL) {
            fprintf(stderr, "Failed to allocate memory for ICONDATA");
            exit(-1);
        }
    }
    for (int i = 0; i < APP_ICON_HEIGHT; i++) {
        for (int j = 0; j < APP_ICON_WIDTH; j++) {
            term->icon[i][j] = term_icon_data[i][j];
        }
    }

    return term;
}
